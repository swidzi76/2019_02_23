package stat;

public class StatApp {
    public static void main(String[] args) {
        System.out.println(" dodawanie 5 + 7 = "+Math.add(5,7));
        System.out.println(" odejmowanie 5 - 7 = "+Math.substract(5,7));
        System.out.println(" wart min 30 i 7  = "+Math.min(30,7));
        System.out.println(" wart max 5 i 7 = "+Math.max(5,7));
        System.out.println(" dzielenie 40 / 7 = "+Math.divide(40,7));
        System.out.println(" mnożenie 40 * 7 = "+Math.multiply(40,7));
        System.out.println(" potęgowanie 2 ^ 8 = "+Math.pow(2,8));
        System.out.println(" pole kołą o promieniu 8 = "+Math.circularField(8));

        // Math math = new Math();
    }
}
